print("for loop using number")
x=[1,2,3,4,5]

for i in x:
    print(i)

print("USING STRING")

fruits=["Apple","Banana","Graphs"]

for fruit in fruits:
    print(fruit)

print("HETEROGENOUS DATA")

studentlist=[[101,"john",85],[102,"jane",95],[103,"deo",89]]

print("***INTERATED THROUGH FOR LOOP***")

for student in studentlist:
    print(student)

print("***ITERATED THROUGH INDEXING***")
print(studentlist[:])
